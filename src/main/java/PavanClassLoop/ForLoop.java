package PavanClassLoop;

public class ForLoop {
    public static void main(String[] args) {


// first example
        int mustafa = 40;
        for (int ayan=0; ayan<=mustafa; ayan++ ){
            System.out.println(ayan);
        }
        // Second example

        for (int kabul=0; kabul<=10; kabul++){
            System.out.println(kabul);

        }
        // third example
        for (int pakistan = 10; pakistan >=0; pakistan--){
            System.out.println(pakistan);

            // note if we know the ending point than we can use for loop
            // and if we do not know the ending point than we use while loop

        }
    }
}
