package PavanClassLoop;

public class While {
    public static void main(String[] args) {
        // Loop is set of statement that can be repeated multiple times based on condition.
        // 1: while loop
        // 2: Do..while loop
        // 3: for loop

        // first example

        int i = 0;
        while (i<=50) {
            System.out.println(i);
            i++;
        }

        // to print even numbers between 0 and 50
         int a = 0;
        while (a <=50){
            System.out.println(a);
            a+=2;
        }

        int ayan = 30;
        while (ayan >=1){
            System.out.println(ayan);
            ayan--;

        }




    }
}
