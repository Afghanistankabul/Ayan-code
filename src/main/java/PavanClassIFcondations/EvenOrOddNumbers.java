public class EvenOrOddNumbers {
    public static void main(String[] args) {

        // Note:
        // we are going to find out the numbers are even numbers or odd numbers
        // if a number divided by 2 and the remaining be 0 than it is even number
        // if a number divided by 2 and the remaining be 1 than it is odd numbers

        int number = 435541;
        if (number % 2 ==0)
            System.out.println("it is even number");
        else
            System.out.println("the number is odd number");


    }
}
